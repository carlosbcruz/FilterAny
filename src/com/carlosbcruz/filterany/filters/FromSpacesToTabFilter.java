	public Behavior[] getSpecialBehavior() {

		return new Behavior[] { Behavior.ACCEPT_AUTOMATIC_BEHAVIOR };
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see
	 * com.carlosbcruz.filterany.FilterAdapter#filter(java.lang.StringBuffer,
	 * java.lang.StringBuffer)
	 */
	@Override
	protected StringBuffer filter(StringBuffer text, StringBuffer auxiliarText)
			throws FilterException {

		this.spacesConverted = 0;

		int numberOfSpaces = NOT_DEFINED;

		StringBuffer output = new StringBuffer();

		String numberOfSpacesParameter = getField1().trim();

		if ("".equals(numberOfSpacesParameter)) { //$NON-NLS-1$

			numberOfSpaces = 4;

		} else {

			try {
				numberOfSpaces = Integer.parseInt(numberOfSpacesParameter);
			} catch (NumberFormatException e) {
				throw new FilterException(Text.get(
						Text.FILTER_FROMSPACESTOTABFILTER_EXCEPTION1,
						String.valueOf(numberOfSpacesParameter)));
			}
			if (numberOfSpaces <= 0) {
				throw new FilterException(Text.get(
						Text.FILTER_FROMSPACESTOTABFILTER_EXCEPTION1,
						String.valueOf(numberOfSpacesParameter)));
			}
		}

		LineTokenizer tokenizer = new LineTokenizer(text);

		while (tokenizer.hasMoreTokens()) {

			String token = tokenizer.nextToken();

			int currentNumberOfSpaces = 0;
			for (int i = 0; i < token.length(); i++) {

				char thisCharacter = token.charAt(i);

				if (thisCharacter == ' ') {

					currentNumberOfSpaces++;

					if (currentNumberOfSpaces == numberOfSpaces) {

						output.append("\t"); //$NON-NLS-1$
						this.spacesConverted += numberOfSpaces;
						currentNumberOfSpaces = 0;
						continue;
					}

					continue;

				}

				if (currentNumberOfSpaces > 0) {

					for (int j = 0; j < currentNumberOfSpaces; j++) {
						output.append(" "); //$NON-NLS-1$
					}

					currentNumberOfSpaces = 0;

					output.append(thisCharacter);

					continue;
				}

				output.append(thisCharacter);
			}

			if (currentNumberOfSpaces > 0) {

				for (int j = 0; j < currentNumberOfSpaces; j++) {
					output.append(" "); //$NON-NLS-1$
				}
				currentNumberOfSpaces = 0;
			}

			if (tokenizer.isFoundNewLine()) {

				output.append(getNewLine());
			}
		}
		return output;
	}

