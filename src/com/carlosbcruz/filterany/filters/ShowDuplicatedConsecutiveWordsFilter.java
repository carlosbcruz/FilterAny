	public String getCommandName() {

		return "DupW";
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see com.carlosbcruz.filterany.Report#getReportText()
	 */
	@Override
	public String getReportText() {

		return Text.get(Text.FILTER_SHOWDUPLICATEDCONSECUTIVEWORDS_REPORT,
				String.valueOf(this.wordsFound));
	}

